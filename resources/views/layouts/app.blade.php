        document.addEventListener("DOMContentLoaded", function () {
            var currentUrl = window.location.href;

            // Mengambil semua elemen <a> dalam <li> di dalam elemen dengan class "header__nav__menu"
            var navLinks = document.querySelector(".header__nav__menu").getElementsByTagName("a");

            // Iterasi melalui setiap tautan navigasi
            for (var i = 0; i < navLinks.length; i++) {
                // Menghapus kelas "active" dari semua elemen <li>
                navLinks[i].parentNode.classList.remove("active");

                // Memeriksa apakah URL saat ini sesuai dengan href tautan navigasi
                if (navLinks[i].href === currentUrl) {
                    // Menambahkan kelas "active" ke elemen <li> yang berisi tautan yang sesuai
                    navLinks[i].parentNode.classList.add("active");
                }
            }
        });
    </script>

